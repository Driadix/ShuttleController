#!/bin/bash
# Скрипт для CI: Проверяет, отформатирован ли код, но НЕ изменяет его.

echo "Проверка форматирования кода (clang-format)..."

# Ищем все исходники и запускаем проверку (флаг --dry-run не меняет файлы, а --Werror возвращает код ошибки 1 при нарушениях)
find Cntrl_V2 -name '*.cpp' -o -name '*.h' -o -name '*.ino' | xargs clang-format --dry-run --Werror

if [ $? -ne 0 ]; then
    echo "[ОШИБКА] Код не отформатирован! Запустите clang-format локально перед коммитом."
    exit 1
fi

echo "[УСПЕХ] Код соответствует стандартам форматирования."